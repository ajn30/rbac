if (isset($_POST['post_announcement'])) {
    $title = $_POST['title'];
    $content = $_POST['content'];
    $user_id = $_SESSION['user_id'];
    $role_id = $_SESSION['role_id'];

    // Insert announcement into database
    $stmt = $conn->prepare("INSERT INTO announcements (user_id, title, content) VALUES (?, ?, ?)");
    $stmt->bind_param("iss", $user_id, $title, $content);

    if ($stmt->execute()) {
        $message = "Announcement posted successfully!";
        log_activity($conn, $user_id, $role_id, 'User ' . $_SESSION['user_name'] . ' posted a new announcement: ' . $title);
    } else {
        $message = "Error posting announcement: " . $stmt->error;
    }
    $stmt->close();
}

